    private void atualizaTabela() {
        try {
            ProdutoDao dao = new ProdutoDao();
            List<Produto> lista = dao.listarProdutos();
            linhaClicada = -1;
            modeloTabela.setLista(lista);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro ao atualizar tabela.", JOptionPane.ERROR_MESSAGE);
        }
    }

