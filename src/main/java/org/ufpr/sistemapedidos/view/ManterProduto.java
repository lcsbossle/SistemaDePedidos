    private void bu_PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_PesquisarActionPerformed
        try {
            String termo = tf_Busca.getText();
            if(termo.equals("")){
                this.atualizaTabela();
            }
            else{
                ProdutoDao dao = new ProdutoDao();
                List<Produto> lista = dao.buscarProdutos(termo);
                modeloTabela.setLista(lista);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bu_PesquisarActionPerformed

    private void tabelaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutosMouseClicked
        linhaClicada = tabelaProdutos.rowAtPoint(evt.getPoint());
        Produto prod = modeloTabela.getProduto(linhaClicada);
        tf_Descricao.setText(prod.getDescricao());
    }//GEN-LAST:event_tabelaProdutosMouseClicked

    private void bu_NovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_NovoActionPerformed
        if(tf_Descricao.getText().equals("")){
            JOptionPane.showMessageDialog(new JFrame(), "Insira uma descrição", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            ProdutoDao dao = new ProdutoDao();
            String desc = tf_Descricao.getText();
            Produto prod = new Produto(desc);
            try {
                dao.incluirProduto(prod);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
            this.atualizaTabela();
        }
    }//GEN-LAST:event_bu_NovoActionPerformed

    private void bu_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_SalvarActionPerformed
        if(linhaClicada==-1){
            JOptionPane.showMessageDialog(new JFrame(), "Selecione um produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        if(tf_Descricao.getText().equals("")){
            JOptionPane.showMessageDialog(new JFrame(), "Insira uma descrição", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            ProdutoDao dao = new ProdutoDao();
            linhaClicada = tabelaProdutos.getSelectedRow();
            Produto prod = modeloTabela.getProduto(linhaClicada);
            Integer id = prod.getId();
        
            try {
                dao.alterarDescricao(id, tf_Descricao.getText());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
            this.atualizaTabela();
        }
    }//GEN-LAST:event_bu_SalvarActionPerformed

    private void bu_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_ExcluirActionPerformed
        
        if(linhaClicada==-1)
        {
            JOptionPane.showMessageDialog(new JFrame(), "Selecione um produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            try {
                ProdutoDao dao = new ProdutoDao();
                linhaClicada = tabelaProdutos.getSelectedRow();
                Produto prod = modeloTabela.getProduto(linhaClicada);
                dao.removerProduto(prod.getId());
            } catch (SQLException ex) {
                Logger.getLogger(ManterProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.atualizaTabela();
        }
    }//GEN-LAST:event_bu_ExcluirActionPerformed

