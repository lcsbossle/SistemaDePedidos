    private void TabelaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaClientesMouseClicked
        linhaClicada = TabelaClientes.rowAtPoint(evt.getPoint());
        Cliente cliente = modeloTabela.getCliente(linhaClicada);
        
        tf_Nome.setText(cliente.getNome());
        tf_Sobrenome.setText(cliente.getSobreNome());
        tf_Cpf.setText(cliente.getCpf());
    }//GEN-LAST:event_TabelaClientesMouseClicked

    private void bu_NovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_NovoActionPerformed
        try{
            ClienteDao dao = new ClienteDao();
            
            String nome = tf_Nome.getText();
            String sobrenome = tf_Sobrenome.getText();
            
            Cpf cpf = new Cpf(tf_Cpf.getText());
            
            if(cpf.isValid())
            {
                Cliente cliente = new Cliente(nome, sobrenome, cpf.toString());
                dao.incluirCliente(cliente);
                this.atualizaTabela();
            }
            else
            {
                JOptionPane.showMessageDialog(new JFrame(), "CPF inv√°lido", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bu_NovoActionPerformed

    private void bu_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_SalvarActionPerformed
        try {
            ClienteDao dao = new ClienteDao();
            
            linhaClicada = TabelaClientes.getSelectedRow();
            Cliente cliente = modeloTabela.getCliente(linhaClicada);
            Integer id = cliente.getId();
            
            Cpf cpf = new Cpf(tf_Cpf.getText());
            if(cpf.isValid())
            {
                dao.alterarCpf(id, cpf.toString());
                dao.alterarNome(id, tf_Nome.getText());
                dao.alterarSobrenome(id, tf_Sobrenome.getText());
            }
            else
            {
                JOptionPane.showMessageDialog(new JFrame(), "CPF inv√°lido", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        this.atualizaTabela();
    }//GEN-LAST:event_bu_SalvarActionPerformed

    private void bu_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_BuscarActionPerformed
        try {
            String termo = tf_Buscar.getText();
            if(termo.equals("")){
                this.atualizaTabela();
            }
            else {
                ClienteDao dao = new ClienteDao();
                List<Cliente> lista = dao.buscarClientes(termo);
                modeloTabela.setListaClientes(lista);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bu_BuscarActionPerformed

