    private void bu_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_ExcluirActionPerformed
        try {
            ClienteDao dao = new ClienteDao();
            PedidoDao pedidoDao = new PedidoDao();
            
            
            
            linhaClicada = TabelaClientes.getSelectedRow();
            Cliente cliente = modeloTabela.getCliente(linhaClicada);
            
            Integer num_pedidos = pedidoDao.listarPedidosDeCliente(cliente).size();
            if(num_pedidos==0)
            {
                dao.excluirCliente(cliente);
            }
            else
            {
                JOptionPane.showMessageDialog(new JFrame(), "N√£o √© poss√≠vel excluir cliente com pedidos", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        this.atualizaTabela();
    }//GEN-LAST:event_bu_ExcluirActionPerformed

